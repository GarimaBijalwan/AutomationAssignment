package Loop_with_Logic;

public class Tables_from_1to15 {

	public static void main(String[] args) 
	    {
		     int i,j;
		     
		    for(i=1;i<=15;i++)
		    {
		    	System.out.println("\n\nTable of "+i+"--");
		    	
		    	for(j=1;j<=10;j++)
		    		System.out.print(" "+i*j);
		    	
		    }
	    }

}
